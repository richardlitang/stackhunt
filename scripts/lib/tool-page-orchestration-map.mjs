import fs from 'node:fs';
import path from 'node:path';

function getFrontmatter(src) {
  const start = src.indexOf('---');
  if (start !== 0) return '';
  const end = src.indexOf('\n---', 3);
  if (end < 0) return '';
  return src.slice(3, end + 1);
}

function findLine(source, index) {
  return source.slice(0, index).split('\n').length;
}

function parseToolPageImports(source) {
  const anchor = "from '@/lib/tool-page'";
  const anchorIndex = source.indexOf(anchor);
  if (anchorIndex < 0) return [];
  const importStart = source.lastIndexOf('import {', anchorIndex);
  if (importStart < 0) return [];
  const openBrace = source.indexOf('{', importStart);
  const closeBrace = source.indexOf('}', openBrace);
  if (openBrace < 0 || closeBrace < 0 || closeBrace > anchorIndex) return [];

  return source
    .slice(openBrace + 1, closeBrace)
    .split(',')
    .map((v) => v.trim())
    .filter(Boolean)
    .map((v) => v.replace(/\s+as\s+.*/, '').trim());
}

function parseIndexExportMap(source) {
  const map = new Map();
  const re = /export\s*{([\s\S]*?)}\s*from\s*['"]([^'"]+)['"];?/g;
  for (const match of source.matchAll(re)) {
    const symbols = match[1]
      .split(',')
      .map((v) => v.trim())
      .filter(Boolean)
      .map((v) => v.replace(/\s+as\s+.*/, '').trim());
    const modulePath = match[2];
    for (const symbol of symbols) map.set(symbol, modulePath);
  }
  return map;
}

function collectRouteCallOrder(frontmatter) {
  const re = /\b((?:build|derive|apply|get|has|is)ToolPage[A-Za-z0-9_]+)\s*\(/g;
  const calls = [];
  for (const match of frontmatter.matchAll(re)) {
    calls.push({
      name: match[1],
      line: findLine(frontmatter, match.index ?? 0),
    });
  }
  return calls;
}

function collectModuleCalls(moduleFile, selfSymbol) {
  if (!fs.existsSync(moduleFile)) return [];
  const src = fs.readFileSync(moduleFile, 'utf8');
  const re = /\b((?:build|derive|apply|get|has|is)ToolPage[A-Za-z0-9_]+)\s*\(/g;
  const found = [];
  for (const match of src.matchAll(re)) {
    const name = match[1];
    if (name !== selfSymbol && !found.includes(name)) found.push(name);
  }
  return found;
}

export function generateToolPageOrchestrationMapMarkdown(rootDir) {
  const routePath = path.join(rootDir, 'src/pages/tool/[slug].astro');
  const indexPath = path.join(rootDir, 'src/lib/tool-page/index.ts');

  const routeSource = fs.readFileSync(routePath, 'utf8');
  const indexSource = fs.readFileSync(indexPath, 'utf8');

  const frontmatter = getFrontmatter(routeSource);
  const imports = parseToolPageImports(frontmatter);
  const exportMap = parseIndexExportMap(indexSource);
  const routeCalls = collectRouteCallOrder(frontmatter);

  const orchestrators = imports
    .filter((name) => /ToolPage/.test(name))
    .map((symbol) => {
      const mod = exportMap.get(symbol) ?? null;
      const moduleFile = mod
        ? path.join(rootDir, mod.replace(/^@\//, 'src/').replace(/\//g, path.sep) + '.ts')
        : null;
      const deps = moduleFile ? collectModuleCalls(moduleFile, symbol) : [];
      return { symbol, mod, deps };
    });

  const lines = [];
  lines.push('# Tool Page Orchestration Map');
  lines.push('');
  lines.push('Generated by `npm run docs:tool-page-map`.');
  lines.push('');
  lines.push(
    'Purpose: provide a fast birds-eye view of `src/pages/tool/[slug].astro` composition flow for troubleshooting and agent onboarding.'
  );
  lines.push('');
  lines.push('## Route Composition Order');
  lines.push('');
  routeCalls.forEach((call, i) => {
    lines.push(`${i + 1}. \`${call.name}(...)\` (frontmatter line ${call.line})`);
  });
  lines.push('');
  lines.push('## Tool-Page Imports Used by Route');
  lines.push('');
  lines.push('| Symbol | Module |');
  lines.push('|---|---|');
  orchestrators.forEach((item) => {
    lines.push(`| \`${item.symbol}\` | \`${item.mod ?? 'unmapped'}\` |`);
  });
  lines.push('');
  lines.push('## One-Level Helper Dependencies');
  lines.push('');
  orchestrators.forEach((item) => {
    lines.push(`### \`${item.symbol}\``);
    if (!item.mod) {
      lines.push('- unresolved in `src/lib/tool-page/index.ts`');
    } else if (item.deps.length === 0) {
      lines.push('- no nested `ToolPage*` helper calls detected');
    } else {
      item.deps.forEach((dep) => lines.push(`- \`${dep}(...)\``));
    }
    lines.push('');
  });

  return `${lines.join('\n')}\n`;
}
